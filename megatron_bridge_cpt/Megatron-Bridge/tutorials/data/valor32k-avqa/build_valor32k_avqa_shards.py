"""Build an Energon WebDataset from preprocessed Valor32k-AVQA v2.0 data.

Runs the full pipeline in one shot:
  1. Build WebDataset tar shards (one sample per QA pair: conversation + video + audio),
     written directly in the flat Energon layout (``{split}-shard-XXXXXX.tar``).
  2. Run ``energon prepare`` to index the shards and create the ``.nv-meta`` metadata
     (``.tar.idx`` per shard, ``index.sqlite``, ``index.uuid``, ``.info.json``, ``split.yaml``).
  3. Write ``.nv-meta/dataset.yaml`` declaring the bridge ``ChatMLWebdataset`` sample type.

Requires megatron-energon >= 7 (e.g. the NeMo 26.06 container). On energon v7 the
``energon prepare`` indexer is reliable; the v6 ``AggregatorPool.close()`` deadlock that
forced a hand-rolled indexer is fixed. ``--skip-dataset-yaml`` is used because the sample
type (``ChatMLWebdataset``) is a Megatron-Bridge class, not an energon built-in, so it
cannot be generated by ``energon prepare --sample-type``.

Usage:
    uv run python tutorials/data/valor32k-avqa/build_valor32k_avqa_shards.py \\
        --data_root /data/valor32k_avqa \\
        --output_dir /data/valor32k_avqa/energon \\
        --samples_per_shard 100
"""

import argparse
import json
import logging
import re
import subprocess
from pathlib import Path


try:
    from tqdm import tqdm
except ImportError:
    tqdm = None

logging.basicConfig(level=logging.INFO, format="%(asctime)s %(levelname)s %(message)s")
logger = logging.getLogger(__name__)

SPLITS = ("train", "val", "test")

# VALOR-32K filenames have the form {youtube_id}_{start.000}_{end.000}.mp4.
# The QA JSON stores only the bare youtube_id, so we need to map back.
_TS_SUFFIX_RE = re.compile(r"^(.+)_\d+\.\d+_\d+\.\d+$")

# dataset.yaml declaring the bridge sample type. energon prepare cannot generate this
# because ChatMLWebdataset is a Megatron-Bridge class, not an energon built-in type.
_DATASET_YAML = """\
__module__: megatron.bridge.data.energon.task_encoder_utils
__class__: ChatMLWebdataset
field_map:
  conversation: conversation.json
  audio: audio.wav
  videos: video.mp4
subflavors: {}
"""


def _build_file_index(directory: Path, suffix: str) -> dict[str, Path]:
    """Map bare youtube_id -> actual file path, stripping the timestamp suffix."""
    index: dict[str, Path] = {}
    for p in directory.glob(f"*{suffix}"):
        m = _TS_SUFFIX_RE.match(p.stem)
        key = m.group(1) if m else p.stem
        index[key] = p
    return index


def build_conversation_json(question: str, answer: str) -> str:
    """Serialize a single QA turn into Energon conversation JSON."""
    conversation = [
        {"role": "user", "content": [{"type": "video"}, {"type": "text", "text": question}]},
        {"role": "assistant", "content": [{"type": "text", "text": answer}]},
    ]
    return json.dumps(conversation)


def build_shards(data_root: Path, output_dir: Path, split: str, samples_per_shard: int) -> int:
    """Write video+audio QA samples into webdataset tar shards for one split.

    Shards are written directly in the flat Energon layout as
    ``{output_dir}/{split}-shard-XXXXXX.tar`` so no restructuring step is needed.

    Returns:
        The number of samples written for this split.
    """
    import webdataset as wds

    qa_file = data_root / f"combined_dataset_{split}_flattened.json"
    if not qa_file.exists():
        logger.warning(f"Skipping {split}: {qa_file} not found")
        return 0

    with open(qa_file) as f:
        qa_pairs = json.load(f)

    # Build youtube_id -> file path indices once per split.
    video_index = _build_file_index(data_root / "videos", ".mp4")
    audio_index = _build_file_index(data_root / "audio", ".wav")
    logger.info(f"  {split}: indexed {len(video_index)} videos, {len(audio_index)} audio files")

    pattern = str(output_dir / f"{split}-shard-%06d.tar")
    written = 0
    skipped = 0

    with wds.ShardWriter(pattern, maxcount=samples_per_shard) as sink:
        items = tqdm(qa_pairs, desc=f"  {split}") if tqdm else qa_pairs
        for qa in items:
            video_id = str(qa["video_id"])
            sample_id = str(qa.get("id", written))
            video_path = video_index.get(video_id)
            audio_path = audio_index.get(video_id)

            if video_path is None or audio_path is None:
                skipped += 1
                continue

            question = qa["question"]
            answer = qa["options"][qa["correct_answer_idx"]]

            sample = {
                "__key__": f"{video_id}_{sample_id}",
                "conversation.json": build_conversation_json(question, answer).encode(),
            }
            with open(video_path, "rb") as vf:
                sample["video.mp4"] = vf.read()
            with open(audio_path, "rb") as af:
                sample["audio.wav"] = af.read()

            sink.write(sample)
            written += 1

    logger.info(f"  {split}: wrote {written} samples, skipped {skipped} (missing video/audio)")
    return written


def run_energon_prepare(output_dir: Path, splits_with_data: list[str], num_workers: int) -> None:
    """Index the shards and create ``.nv-meta`` metadata via ``energon prepare``.

    Uses regex ``--split-parts`` patterns (``{split}-shard-.*``) to assign each shard
    to its split by filename prefix, so the train/val/test boundaries from the source
    annotations are preserved exactly (no random re-splitting).

    Note:
        The pattern is brace-expanded then compiled as a *regex*, so ``.*`` (not a glob
        ``*``) is required. ``--skip-dataset-yaml`` is used because the sample loader is
        written separately by :func:`write_dataset_yaml`.
    """
    cmd = [
        "energon",
        "prepare",
        str(output_dir),
        "--non-interactive",
        "--num-workers",
        str(num_workers),
        "--skip-dataset-yaml",
        "--force-overwrite",
    ]
    for split in splits_with_data:
        cmd += ["--split-parts", f"{split}:{split}-shard-.*"]

    logger.info("  running: %s", " ".join(cmd))
    subprocess.run(cmd, check=True)


def write_dataset_yaml(output_dir: Path) -> None:
    """Write ``.nv-meta/dataset.yaml`` declaring the bridge ChatMLWebdataset sample type."""
    dataset_yaml = output_dir / ".nv-meta" / "dataset.yaml"
    dataset_yaml.write_text(_DATASET_YAML)
    logger.info(f"  wrote {dataset_yaml}")


def main() -> None:
    """Run the full Energon dataset build pipeline for Valor32k-AVQA."""
    parser = argparse.ArgumentParser(description="Build Energon dataset from Valor32k-AVQA")
    parser.add_argument("--data_root", type=str, required=True)
    parser.add_argument("--output_dir", type=str, required=True)
    parser.add_argument("--samples_per_shard", type=int, default=100)
    parser.add_argument(
        "--num_workers",
        type=int,
        default=8,
        help="Number of workers energon prepare uses to index tar files.",
    )
    args = parser.parse_args()

    data_root = Path(args.data_root)
    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    logger.info("Step 1/3: building WebDataset shards")
    splits_with_data = []
    for split in SPLITS:
        if build_shards(data_root, output_dir, split, args.samples_per_shard) > 0:
            splits_with_data.append(split)

    if not splits_with_data:
        raise RuntimeError(f"No shards were written; check that {data_root} contains the expected data.")

    logger.info("Step 2/3: indexing shards with energon prepare")
    run_energon_prepare(output_dir, splits_with_data, args.num_workers)

    logger.info("Step 3/3: writing .nv-meta/dataset.yaml")
    write_dataset_yaml(output_dir)

    logger.info(f"Done. Energon dataset ready at: {output_dir}")


if __name__ == "__main__":
    main()
